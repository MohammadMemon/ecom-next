export const  catchAsyncErrors = (theFunc) => async (request, context) => {
  try {
    return await theFunc(request, context);
  } catch (error) {
    return new Response(
      JSON.stringify({
        success: false,
        message: error.message || "Internal Server Error",
      }),
      { status: error.statusCode || 500 }
    );
  }
};


